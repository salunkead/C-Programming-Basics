// escape sequence in c
#include <stdio.h>
void main()
{
    printf("Amol\nSalunke\n"); // new line
    printf("----------------------\n");
    printf("Amol\bSalunke\n"); // back space
    printf("----------------------\n");
    printf("Amol\tSalunke\n"); // tab horizontal
    printf("----------------------\n");
    printf("Amol\vSalunke\n"); // tab vertical
}

/*
escape sequence in c programming is a sequence of characters
escape sequence are useful for controlling the formatting of the output and including special characters in string.
it is composed of two things backslash(\) and a character
escapeSequence  meaning
        \n       new line
        \t       tab(horizontal)
        \v       tab(vertical)
        \b       backspace
*/
